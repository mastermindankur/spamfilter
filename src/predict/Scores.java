package predict;

public class Scores {

	private float scoreCTF;
	private float scoreDF;
	private String mailno;
	
	Scores(String no,float CTF,float DF)
	{
		this.mailno=no;
		this.scoreCTF=CTF;
		this.scoreDF=DF;
	}

	public float getScoreCTF() {
		return scoreCTF;
	}
	public String getMailno() {
		return mailno;
	}
	public void setMailno(String mailno) {
		this.mailno = mailno;
	}
	public void setScoreCTF(float scoreCTF) {
		this.scoreCTF = scoreCTF;
	}
	public float getScoreDF() {
		return scoreDF;
	}
	public void setScoreDF(float scoreDF) {
		this.scoreDF = scoreDF;
	}



}
