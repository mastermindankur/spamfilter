package ngrams;

public class BiGram_POJO {
	
	public BiGram_POJO(String bigram2, int count2, float probability2) {
		this.bigram=bigram2;
		this.counter=count2;
		this.probability=probability2;
	}
	public String getBigram() {
		return bigram;
	}
	public void setBigram(String bigram) {
		this.bigram = bigram;
	}
	public int getCounter() {
		return counter;
	}
	public void setCounter(int count) {
		this.counter = count;
	}
	public float getProbability() {
		return probability;
	}
	public void setProbability(float probability) {
		this.probability = probability;
	}
	private String bigram;
	private int counter;
	private float probability;
	
	

}
